class Command(BaseCommand):
    help = 'Xóa sạch và rebuild toàn bộ RouteNode và RouteEdge theo hình mẫu'

    def add_arguments(self, parser):
        parser.add_argument('--confirm', action='store_true',
                            help='Xác nhận xóa sạch dữ liệu cũ và rebuild')
        parser.add_argument('--step', type=float, default=5.0,
                            help='Khoảng cách node (mét), mặc định 5.0m')

    def handle(self, *args, **options):
        if not options['confirm']:
            self.stdout.write(
                'WARNING: Lenh nay se XOA SACH toan bo RouteNode va RouteEdge.\n'
                'Chay lai voi --confirm de xac nhan:\n'
                '  python manage.py rebuild_nodes --confirm'
            )
            return

        step_m = options['step']
        self.stdout.write(f'=== REBUILD CAMPUS GRAPH (step={step_m}m) ===')

        # ---- BƯỚC 1: Xóa sạch ----
        old_edges = RouteEdge.objects.count()
        old_nodes = RouteNode.objects.count()
        RouteEdge.objects.all().delete()
        RouteNode.objects.all().delete()
        self.stdout.write(f'Da xoa {old_edges} edges va {old_nodes} nodes cu.')

        # ---- BƯỚC 2: Sinh điểm & Snap & Tạo Nodes/Edges ----
        created_nodes = []  # list of RouteNode
        created_edges_set = set()  # set of (node_a_id, node_b_id) sorted tuple
        MERGE_DIST = 3.5
        edges_created_count = 0

        # Helper to find close node
        def find_existing_node(lat, lng):
            for node in created_nodes:
                if haversine(lat, lng, float(node.latitude), float(node.longitude)) < MERGE_DIST:
                    return node
            return None

        self.stdout.write('Dang tao nodes va edges tu walkway paths...')
        for idx, path in enumerate(WALKWAY_PATHS):
            raw_coords = sample_path(path, step_m=step_m)
            path_nodes = []
            for lat, lng in raw_coords:
                # Lọc điểm nằm trong building
                inside = any(point_in_polygon(lat, lng, poly) for poly in BUILDING_POLYGONS)
                if inside:
                    continue
                # Snap/Merge
                existing_node = find_existing_node(lat, lng)
                if existing_node:
                    path_nodes.append(existing_node)
                else:
                    new_node = RouteNode.objects.create(latitude=lat, longitude=lng, name=None)
                    created_nodes.append(new_node)
                    path_nodes.append(new_node)
            
            # Nối các node liên tiếp trong path này thành các cạnh
            for i in range(len(path_nodes) - 1):
                na = path_nodes[i]
                nb = path_nodes[i + 1]
                if na.id == nb.id:
                    continue
                pair = tuple(sorted((na.id, nb.id)))
                if pair not in created_edges_set:
                    created_edges_set.add(pair)
                    dist = haversine(na.latitude, na.longitude, nb.latitude, nb.longitude)
                    lat_a, lng_a = float(na.latitude), float(na.longitude)
                    lat_b, lng_b = float(nb.latitude), float(nb.longitude)
                    
                    # Cạnh thuận
                    RouteEdge.objects.create(
                        node_a=na, node_b=nb,
                        distance=round(dist, 2),
                        points=[[lat_a, lng_a], [lat_b, lng_b]],
                        is_active=True
                    )
                    # Cạnh nghịch
                    RouteEdge.objects.create(
                        node_a=nb, node_b=na,
                        distance=round(dist, 2),
                        points=[[lat_b, lng_b], [lat_a, lng_a]],
                        is_active=True
                    )
                    edges_created_count += 1

        self.stdout.write(f'Da tao {len(created_nodes)} RouteNode va {edges_created_count} RouteEdge (bidirectional).')

        # ---- BƯỚC 3: Validation ----
        self.stdout.write('\n=== VALIDATION ===')
        all_nodes = RouteNode.objects.all()
        in_building = 0
        outside_campus = 0
        for node in all_nodes:
            lat_f, lng_f = float(node.latitude), float(node.longitude)
            if not is_point_in_campus(lat_f, lng_f):
                outside_campus += 1
            if any(point_in_polygon(lat_f, lng_f, poly) for poly in BUILDING_POLYGONS):
                in_building += 1

        # Check connectivity (BFS)
        connected = False
        isolated_count = 0
        if created_nodes:
            adj = {n.id: set() for n in created_nodes}
            for na_id, nb_id in created_edges_set:
                adj[na_id].add(nb_id)
                adj[nb_id].add(na_id)
            
            start_id = created_nodes[0].id
            visited = {start_id}
            queue = [start_id]
            while queue:
                curr = queue.pop(0)
                for neighbor in adj[curr]:
                    if neighbor not in visited:
                        visited.add(neighbor)
                        queue.append(neighbor)
            isolated_nodes = [n.id for n in created_nodes if n.id not in visited]
            isolated_count = len(isolated_nodes)
            connected = (isolated_count == 0)

        self.stdout.write(f'  Tong nodes: {all_nodes.count()}')
        self.stdout.write(f'  Trong toa nha: {in_building}')
        self.stdout.write(f'  Ngoai campus: {outside_campus}')
        self.stdout.write(f'  Lien thong (connected): {connected}')
        if not connected:
            self.stdout.write(f'  [WARN] Co {isolated_count} node co lap/khong lien thong.')

        self.stdout.write('\n=== REBUILD HOAN THANH ===')

